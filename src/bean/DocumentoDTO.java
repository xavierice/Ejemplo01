package bean;



public class DocumentoDTO{
	
private int cod_documento;
private int cod_persona;
private int codigo_tramite;
private String fecha;
private int cod_estado;
private String observacion;
private int area_actual;
public int getCod_documento() {
	return cod_documento;
}
public void setCod_documento(int cod_documento) {
	this.cod_documento = cod_documento;
}
public int getCod_persona() {
	return cod_persona;
}
public void setCod_persona(int cod_persona) {
	this.cod_persona = cod_persona;
}
public int getCodigo_tramite() {
	return codigo_tramite;
}
public void setCodigo_tramite(int codigo_tramite) {
	this.codigo_tramite = codigo_tramite;
}
public String getFecha() {
	return fecha;
}
public void setFecha(String fecha) {
	this.fecha = fecha;
}
public int getCod_estado() {
	return cod_estado;
}
public void setCod_estado(int cod_estado) {
	this.cod_estado = cod_estado;
}
public String getObservacion() {
	return observacion;
}
public void setObservacion(String observacion) {
	this.observacion = observacion;
}
public int getArea_actual() {
	return area_actual;
}
public void setArea_actual(int area_actual) {
	this.area_actual = area_actual;
}



}
